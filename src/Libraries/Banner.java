package Libraries;

public class Banner {
    public void createBanner() {
        final String BLUE  = "\u001B[94m";
        final String WHITE = "\u001B[36m";
        final String RESET = "\u001B[0m";

        String[] banner = {
        "________/\\\\\\\\\\\\\\\\\\_________________/\\\\\\\\\\\\___________________/\\\\\\_______/\\\\\\_        \n",
         " _____/\\\\\\////////_________________\\////\\\\\\__________________\\///\\\\\\___/\\\\\\/__       \n",
         "  ___/\\\\\\/_____________________________\\/\\\\\\____________________\\///\\\\\\\\\\\\/____      \n",
         "   __/\\\\\\______________/\\\\\\\\\\\\\\\\\\_______\\/\\\\\\________/\\\\\\\\\\\\\\\\_____\\//\\\\\\\\______     \n",
         "    _\\/\\\\\\_____________\\////////\\\\\\______\\/\\\\\\______/\\\\\\//////_______\\/\\\\\\\\______    \n",
         "     _\\//\\\\\\______________/\\\\\\\\\\\\\\\\\\\\_____\\/\\\\\\_____/\\\\\\______________/\\\\\\\\\\\\_____   \n",
         "      __\\///\\\\\\___________/\\\\\\/////\\\\\\_____\\/\\\\\\____\\//\\\\\\___________/\\\\\\////\\\\\\___  \n",
         "       ____\\////\\\\\\\\\\\\\\\\\\_\\//\\\\\\\\\\\\\\\\/\\\\__/\\\\\\\\\\\\\\\\\\__\\///\\\\\\\\\\\\\\\\__/\\\\\\/___\\///\\\\\\_ \n",
         "        _______\\/////////___\\////////\\//__\\/////////_____\\////////__\\///_______\\///__"
        };
        

        for (String line : banner) {
            StringBuilder colored_banner = new StringBuilder();
            for (char c : line.toCharArray()) {
                if (c == '_') {
                    colored_banner.append(WHITE).append(c).append(RESET);
                } else if (c == '/' || c == '\\') {
                    colored_banner.append(BLUE).append(c).append(RESET);
                } else {
                    colored_banner.append(c);
                }
            }
            System.out.print(colored_banner);
        }
    }
}
